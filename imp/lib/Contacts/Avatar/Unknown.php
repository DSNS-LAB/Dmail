class IMP_Contacts_Avatar_Unknown implements IMP_Contacts_Avatar_Backend
{
    /**
     */
    public function avatarImg($email)
    {
        $img = Horde_Themes::img('unknown_contact.jpg');
        return array(
            'desc' => '',
            'url' => $img->uri
        );
    }

